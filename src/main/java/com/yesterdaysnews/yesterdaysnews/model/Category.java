package com.yesterdaysnews.yesterdaysnews.model;

public class Category {
    private int id;
    private String type;


public Category(){}
    public int getId() {
        return this.id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return this.type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
